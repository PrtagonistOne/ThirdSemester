using System;

namespace Problem_1
{
    class MathOperations
    {
        public int  Add(int a, int b)
        {

            return a + b;
        }

        public double Add(double a, double b, double z)
        {

            return a + b + z;
        }

        public decimal Add(decimal a, decimal b, decimal z)
        {

            return a + b + z;
        }
    }
}
